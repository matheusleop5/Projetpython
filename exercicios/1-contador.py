#Exercício 1: Contador crescente e decrescente
# Objetivo: Usar while para criar um contador que imprima números de 1 a 10 (crescente) e depois de 10 a 1 (decrescente).

print("Contador crescente:")
i = 0
while i <= 10 :
    print(i)
    i += 1

print("Contador decrescente:")
j = 10
while j >= 0 :
    print(j)
    j -= 2